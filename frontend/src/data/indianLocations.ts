// India geographic boundaries for map restriction
export const indiaBounds = {
  north: 35.513327,
  south: 6.753516,
  east: 97.395561,
  west: 68.162386,
  center: { lat: 20.5937, lng: 78.9629 },
};

// Helper function to check if coordinates are within India
export function isWithinIndia(lat: number, lng: number): boolean {
  return (
    lat >= indiaBounds.south &&
    lat <= indiaBounds.north &&
    lng >= indiaBounds.west &&
    lng <= indiaBounds.east
  );
}

// Get districts for a state
export function getDistrictsByState(stateCode: string): string[] {
  const state = indianStates.find((s) => s.code === stateCode);
  return state?.districts || [];
}
